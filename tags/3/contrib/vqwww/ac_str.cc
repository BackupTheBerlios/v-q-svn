#include <exception>
#include <iostream>
#include <fstream>
#include <string>
#include <cerrno>

#include "conf_home.h"

using namespace std;

void usage(const char *me) {
	cerr<<"usage: "<<me<<" name [del_lf]"<<endl;
}

int main(int ac, char **av) {
	try {
			if(ac<2) {
					usage(*av);
					return 1;
			}
		
			string file;
			string ln;
			while(getline(cin, ln)) {
					file += ln;
					if( ac != 3 ) file += '\n';
			}

			if( file.empty() ) {
					cerr<<"string is empty"<<endl;
					return 1;
			}

			string ac_conf("auto/");
			ac_conf+=av[1];
			ac_conf+=".cc";
			
			ofstream out(ac_conf.c_str(), ios::trunc);
			out<<"// file autogenerated, don't edit!"<<endl
				<<"#include \"../cconf.h\""<<endl
				<<"clnconf ac_"<<av[1]<<"(\""<<conf_home
				<<"/etc/vqwww/"<<av[1]<<"\",\"";

			string::const_iterator iter, end=file.end();
			for( iter=file.begin(); iter!=end; ++iter ) {
					out<< "\\x" << hex << (int) *iter;
			}

			out<<"\");"<<endl;
			out.close();
			if(! out) {
				cerr<<"can't write: "<<ac_conf<<": "<<strerror(errno)<<endl;
				return 1;
			}
	} catch( const exception & e ) {
			cerr<<"exception: "<<e.what()<<endl;
			return 1;
	}
	return 0;
}
